void try_states(int cur_state, int flag_index)
{
    int flag_char = flag_prefix_buf[flag_index];

    for (int i=0; i<256; i++)
    {
        Node node = states[cur_state][i];
        if (node.from == (int)flag_char){
            flag_postfix_buf[flag_index] = (char)node.to;
            build_states(node.next_state, flag_index+1);
            flag_postfix_buf[flag_index] = 0;
        }
    }
}

void print_flag_postfix()
{
    for (int i=0; i<FLAG_PREFIX_LEN; i++)
        if ((int)flag_postfix_buf[i])
            printf("%c", flag_postfix_buf[i]);
    printf("\n");
}

void build_states(int cur_state, int flag_index)
{
    if (flag_index != FLAG_PREFIX_LEN-1) 
    {
        try_states(cur_state, flag_index);
        return;
    }
    if (cur_state != FINAL_STATE)
        return;
    print_flag_postfix();
    exit(0);
}

